package day2;

public class PreOrPostIncrement {
	public static void main(String[] args) {
		int x=10;
		System.out.println(x++);
		System.out.println(x);
		x=10;
		System.out.println(++x);
		System.out.println(x);
		x=10;
		System.out.println(x--);
		System.out.println(x);
		x=10;
		System.out.println(--x);
		System.out.println(x);
	}
	

}
